// Cari dil
let currentLang = 'az';

// Tərcümə funksiyası
function translatePage(lang) {
    currentLang = lang;
    const translation = translations[lang];
    
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translation[key]) {
            element.textContent = translation[key];
        }
    });
    
    const langToggle = document.getElementById('languageToggle');
    if (langToggle) {
        const span = langToggle.querySelector('span');
        if (span) {
            span.textContent = lang.toUpperCase();
        }
    }
    
    localStorage.setItem('preferredLanguage', lang);
}

// FAQ funksiyası
function setupFAQ() {
    const faqItems = document.querySelectorAll('.faq-item');
    
    faqItems.forEach(item => {
        const question = item.querySelector('.faq-question');
        
        question.addEventListener('click', () => {
            // Bağlı olan digər FAQ item-larını bağla
            faqItems.forEach(otherItem => {
                if (otherItem !== item) {
                    otherItem.classList.remove('active');
                }
            });
            
            // Cari item-ı aç/bağla
            item.classList.toggle('active');
        });
    });
}

// Scroll to top funksiyası
function setupScrollButton() {
    const scrollBtn = document.getElementById('scrollTopBtn');
    
    window.addEventListener('scroll', function() {
        if (window.scrollY > 300) {
            scrollBtn.style.display = 'flex';
        } else {
            scrollBtn.style.display = 'none';
        }
    });
    
    scrollBtn.addEventListener('click', function() {
        window.scrollTo({top: 0, behavior: 'smooth'});
    });
}

// Dropdown funksiyaları
function setupDropdowns() {
    const languageToggle = document.getElementById('languageToggle');
    const languageMenu = document.getElementById('languageMenu');
    const profileToggle = document.getElementById('profileToggle');
    const profileMenu = document.getElementById('profileMenu');
    
    if (languageToggle && languageMenu) {
        languageToggle.addEventListener('click', function(e) {
            e.stopPropagation();
            languageMenu.style.display = languageMenu.style.display === 'block' ? 'none' : 'block';
            if (profileMenu) profileMenu.style.display = 'none';
        });
    }
    
    if (profileToggle && profileMenu) {
        profileToggle.addEventListener('click', function(e) {
            e.stopPropagation();
            profileMenu.style.display = profileMenu.style.display === 'block' ? 'none' : 'block';
            if (languageMenu) languageMenu.style.display = 'none';
        });
    }
    
    document.querySelectorAll('.language-menu a').forEach(link => {
        link.addEventListener('click', function(e) {
            e.preventDefault();
            const lang = this.getAttribute('data-lang');
            translatePage(lang);
            if (languageMenu) languageMenu.style.display = 'none';
        });
    });
    
    document.addEventListener('click', function() {
        if (languageMenu) languageMenu.style.display = 'none';
        if (profileMenu) profileMenu.style.display = 'none';
    });
    
    if (languageMenu) {
        languageMenu.addEventListener('click', function(e) {
            e.stopPropagation();
        });
    }
    
    if (profileMenu) {
        profileMenu.addEventListener('click', function(e) {
            e.stopPropagation();
        });
    }
}

// Səhifə yüklənəndə
document.addEventListener('DOMContentLoaded', function() {
    const savedLang = localStorage.getItem('preferredLanguage') || 'az';
    translatePage(savedLang);
    
    setupDropdowns();
    setupScrollButton();
    setupFAQ();
    
    // Auth button
    const authBtn = document.querySelector('.auth-btn');
    if (authBtn) {
        authBtn.addEventListener('click', function() {
            alert(currentLang === 'az' ? 'Giriş/Qeydiyyat səhifəsinə yönləndirilir...' :
                  currentLang === 'en' ? 'Redirecting to Login/Registration page...' :
                  'Перенаправление на страницу Входа/Регистрации...');
        });
    }
    
    // Nav link-lər
    document.querySelectorAll('.nav-link').forEach(link => {
        link.addEventListener('click', function(e) {
            if (this.getAttribute('href') === '#') {
                e.preventDefault();
                alert(currentLang === 'az' ? 'Bu səhifə hazırlanma prosesindədir...' :
                      currentLang === 'en' ? 'This page is under construction...' :
                      'Эта страница в разработке...');
            }
        });
    });
});